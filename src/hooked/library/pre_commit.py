from __future__ import annotations

import os

from hooked.library import install
from hooked.library.cmd import CommandError, run_cmd, run_stream
from hooked.library.logger import logger


def pre_commit_version():
    version = run_cmd(["pre-commit", "--version"]).stdout
    logger.debug(f"running {version}")


def pre_commit_run(
    config: str, env: dict[str, str] | None = None, cwd: str | None = None
) -> None:
    try:
        _env = os.environ.copy() if not env else env
        _env["PRE_COMMIT_COLOR"] = "always"
        run_stream(["pre-commit", "run", "--config", config], env=_env, cwd=cwd)
    except CommandError as e:
        logger.error(f"Error while run pre-commit: {e}")
        raise


def pre_commit_install(repo: str) -> None:
    try:
        install.disable()
        run_cmd(["pre-commit", "install"], cwd=repo)
    except CommandError as e:
        logger.error(f"Error while installing local pre-commit hook: {e}")
        raise
    finally:
        install.enable()


def is_pre_commit_available(repo: str) -> bool:
    """Check if a pre-commit config exists in the repository"""
    config_path = os.path.join(repo, ".pre-commit-config.yaml")

    return os.path.isfile(config_path)


def is_pre_commit_installed(repo: str) -> bool:
    """Check if a pre-commit hook exists and was generated by pre-commit framework."""
    hook_path = os.path.join(repo, ".git/hooks/pre-commit")

    if not os.path.isfile(hook_path):
        return False

    try:
        with open(hook_path) as file:
            content = file.read()
            return "File generated by pre-commit: https://pre-commit.com" in content
    except:
        return False


def pre_commit_autoupdate(repo: str) -> None:
    """Do an pre-commit autoupdate on a repository"""
    try:
        run_cmd(["pre-commit", "autoupdate"], cwd=repo)
    except CommandError as e:
        logger.error(f"Error while autoupdate: {e}")
        raise
